/* ── Global error boundary — гарантирует видимость app при любом crash ── */
window.addEventListener('error', function(e) {
  console.error('[GLOBAL]', e.message, e.filename, e.lineno);
  var app = document.getElementById('app');
  if (app && !app.classList.contains('ready')) app.classList.add('ready');
});
window.addEventListener('unhandledrejection', function(e) {
  console.error('[GLOBAL] Unhandled promise:', e.reason);
  var app = document.getElementById('app');
  if (app && !app.classList.contains('ready')) app.classList.add('ready');
});

